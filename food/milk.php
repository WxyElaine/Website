                <div class="tip">
                    <h2>饮用小贴士</h2>
                    <ul>
                        <li>
                            <b>不要直接喝生牛奶</b></br>
                            没有经过任何加工处理的生牛奶（Raw milk），可能含有多种微生物，
                            其中不少是致病菌，<b>贸然饮用有很大风险</b>。
                        </li>
                        <li>
                            <b>不空腹喝牛奶</b></br>
                            空腹吃东西会导致肠胃蠕动较快，奶中营养<b>不易消化吸收</b>。
                        </li>
                        <li>
                            <b>不用牛奶服药</b></br>
                            牛奶易在表面形成一个覆盖酸，使奶中的钙与药物发生化学反应。
                        </li>
                        <li>
                            <b>不宜多饮冰牛奶</b></br>
                            冰牛奶会影响肠胃的运动机能，引起轻度腹泻，使牛奶中的营养成分不能被充分吸收。
                        </li>
                        <li>
                            <b>不宜长时间高温蒸煮</b></br>
                            牛奶中的蛋白质经高温煮沸后，其中的蛋白质会由溶胶转化成凝胶状态，<b>营养价值就会降低</b>。
                        </li>
                        <li>
                            如果喝了牛奶之后会出现<b>胀气</b>、<b>腹痛</b>、<b>腹泻</b>等症状，
                            可能是患有乳糖不耐受症，应该适当掌握牛奶的摄入量。
                        </li>
