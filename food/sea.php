                    <h3>泡发干海参</h3>
                    <ul>
                        <li>使用无油无盐的容器，如大的玻璃碗；</li>
                        <li>将冷水放入玻璃碗里，浸没海参；</li>
                        <li>每天换水一次；</li>
                        <li>将玻璃碗在冰箱里放置2天；</li>
                        <li>第三天将海参放入水煮沸30分钟，静置放凉；</li>
                        <li>如果海参已经变软，即可烹饪，否则重复煮海参的步骤。</li>
